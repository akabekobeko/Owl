
namespace Owl
{
	/// <summary>
	/// タグの内容となるデータ型を表します。
	/// </summary>
	public enum TagDataType
	{
		/// <summary>
		/// 文字列。
		/// </summary>
		String,

		/// <summary>
		/// 32 ビット整数。
		/// </summary>
		Int32,

		/// <summary>
		/// 64 ビット整数。
		/// </summary>
		Int64,

		/// <summary>
		/// 時間。
		/// </summary>
		TimeSpan,

		/// <summary>
		/// 日時。
		/// </summary>
		DateTime,

		/// <summary>
		/// 画像。
		/// </summary>
		Picture
	}
}
